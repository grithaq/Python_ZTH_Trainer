# Python Operator Aritmatika

x = 10
y = 5

# 1. Penjumlahan (+)
print(f"{x} + {y} = {x+y} \n")

# 2. Pengurangan (-)
print(f"{x} - {y} = {x-y} \n")

# 3. Perkalian (*)
print(f"{x} * {y} = {x*y} \n")

# 4.Pembagian (/)
print(f"{x} / {y} = {x/y} \n")

# 5. Exponensial (**)
print(f"{x} * {y} = {x**y} \n")

# 6. Modulus / Sisa Bagi (%)
print(f"{x} % {y} = {x%y} \n")

# 7. Division Flor (//)
print(f"{x} // {y} = {x//y} \n")



