nilai = 100

# 2. Penjumlahan
nilai+= 20
print(f"Nilai + 20 : {nilai}")

# 3. Pengurangan

nilai-=5
print(f"Nilai - 5 : {nilai}")

# 4. Perkalian
nilai*=5
print(f"Nilai * 5 : {nilai}")

# 5. Pembagian
nilai/=5
print(f"Nilai / 5 : {nilai}")

# 6. Exponensial
nilai**=5
print(f"Nilai ** 5 : {nilai}")

# 7. Modulus
nilai%=5
print(f"Nilai % 5 : {nilai}")

# 8.Division Flor

nilai//=5
print(f"Nilai // 5 : {nilai}")
