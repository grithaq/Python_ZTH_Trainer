x = 10
y = 5

# 1. Equal /Sama dengan ==
print(f"{x} = {y} : {x==y} \n")

# 2. Not Equal (!=)
print(f"{x} != {y} : {x!=y} \n")

# 3. Grater Than / Lebih Besar (>)
print(f"{x} > {y} : {x>y} \n")

# 4. Less Than / Lebih kecil dari (<)
print(f"{x} < {y} : {x<y} \n")

# 5. Grater Than or Equal to / Lebih besar sama dengan (>=)
print(f"{x} >= {y} : {x>=y} \n")

# 6. Less Than or Equal to / Lebih kecil sama dengan (<=)
print(f"{x} <= {y} : {x<=y} \n")



